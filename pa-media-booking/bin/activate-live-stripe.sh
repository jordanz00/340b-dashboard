#!/bin/bash
# Deploy live Stripe keys to pamedia.art (mu-plugin + optional WP settings).
#
# Usage:
#   export SFTP_USER='...' SFTP_PASS='...'
#   export STRIPE_PK='pk_live_...' STRIPE_SK='sk_live_...'
#   ./pa-media-booking/bin/activate-live-stripe.sh
#
# Keys: Stripe Dashboard → Developers → API keys → Test mode OFF.

set -euo pipefail

PK="${STRIPE_PK:-}"
SK="${STRIPE_SK:-}"

if [[ "$PK" != pk_live_* ]]; then
  echo "STRIPE_PK must start with pk_live_ (turn off Test mode in Stripe)."
  exit 1
fi
if [[ "$SK" != sk_live_* ]]; then
  echo "STRIPE_SK must start with sk_live_ (matching live secret key)."
  exit 1
fi

ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
MU="/tmp/pa-booking-stripe-keys.php"

cat > "$MU" <<PHP
<?php
/**
 * Live Stripe keys for PA Media Booking — do not commit.
 * Generated by bin/activate-live-stripe.sh
 */
if (!defined('ABSPATH')) {
    exit;
}

define('PA_BOOKING_STRIPE_PK', '${PK}');
define('PA_BOOKING_STRIPE_SK', '${SK}');
PHP

chmod 600 "$MU"
echo "Wrote mu-plugin (mode: live)."

if [[ -z "${SFTP_USER:-}" || -z "${SFTP_PASS:-}" ]]; then
  echo "SFTP_USER/SFTP_PASS not set — upload $MU manually to wp-content/mu-plugins/"
  exit 0
fi

bash "$ROOT/deploy-sftp.sh"

echo ""
echo "Verify live mode:"
curl -sL 'https://pamedia.art/wp-json/pa-booking/v1/availability?month=2026-06' | python3 -c "
import json,sys
d=json.load(sys.stdin)
print('stripe_ready:', d.get('stripe_ready'))
print('stripe_mode:', d.get('stripe_mode','(deploy plugin v2.4+ for mode field)'))
"
